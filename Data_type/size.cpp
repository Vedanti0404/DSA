//Write a program to find the size of each fundamental data type in C++.
#include<bits/stdc++.h>
using namespace std;
int main(){
    cout<<sizeof(int)<<"byte"<<endl;
    cout<<sizeof(long int)<<"byte"<<endl;
    cout<<sizeof(long long int)<<"byte"<<endl;
    cout<<sizeof(float)<<"byte"<<endl;
    cout<<sizeof(double)<<"byte"<<endl;
    cout<<sizeof(char)<<"byte"<<endl;
    cout<<sizeof(bool)<<"byte"<<endl;
    cout<<sizeof(string)<<"byte"<<endl;
    

return 0;
}